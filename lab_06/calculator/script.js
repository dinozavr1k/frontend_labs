let first = Number(prompt ("Enter the first number"));

let sign = prompt ("Enter mathematic sign");

let second = Number(prompt ("Enter the second number"));



if (sign == '*') {
    alert(first * second)
}

else if (sign == '/') {
    alert(first / second)
}

else if (sign == '-') {
    alert(first - second)
}

else if (sign == '+') {
    alert(first + second)
}





